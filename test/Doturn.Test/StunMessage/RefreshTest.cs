using Xunit;

namespace Doturn.StunMessage.Test
{
    public class RefreshTest
    {
        private readonly byte[] transactionId = new byte[] { 0x39, 0x50, 0x4d, 0x4b, 0x64, 0x63, 0x79, 0x30, 0x6e, 0x6c, 0x69, 0x58 };
        private readonly byte[] refreshRequestByteArray = new byte[] {
            0x00, 0x0D, 0x00, 0x04, 0x00, 0x00, 0x02, 0x58, // Lifetime
            0x00, 0x06, 0x00, 0x08, 0x75, 0x73, 0x65, 0x72, 0x6E, 0x61, 0x6D, 0x65, // Username username
            0x00, 0x14, 0x00, 0x0B, 0x65, 0x78, 0x61, 0x6D, 0x70, 0x6C, 0x65, 0x2E, 0x63, 0x6F, 0x6D, 0x00, // Realm example.com
            0x00, 0x15, 0x00, 0x10, 0x68, 0x6F, 0x32, 0x79, 0x64, 0x77, 0x35, 0x71, 0x65, 0x65, 0x71, 0x73, 0x67, 0x61, 0x73, 0x7A, // Nonce ho2ydw5qeeqsgasz
            0x80, 0x28, 0x00, 0x04, 0x76, 0xbf, 0x9e, 0x09 // Fingerprint (incorrect!!)
        };
        private readonly byte[] refreshSuccessResponseByteArray = new byte[] {
            0x01, 0x04, 0x00, 0x38, 0x21, 0x12, 0xA4, 0x42, 0x39, 0x50, 0x4D, 0x4B, 0x64, 0x63, 0x79, 0x30, 0x6E, 0x6C, 0x69, 0x58, // header
            0x00, 0x0D, 0x00, 0x04, 0x00, 0x00, 0x02, 0x58, // Lifetime
            0x80, 0x22, 0x00, 0x06, 0x44, 0x6F, 0x74, 0x75, 0x72, 0x6E, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // Software
            0x00, 0x08, 0x00, 0x14, 0x68, 0x64, 0x6D, 0xAF, 0x78, 0x20, 0x43, 0x2B, 0x4B, 0x5E, 0x3E, 0x9E, 0x91, 0x7E, 0x13, 0xF4, 0x4E, 0x10, 0xD0, 0x79, // MessageIntegrity
            0x80, 0x28, 0x00, 0x04, 0x16, 0xC9, 0x78, 0x1E // Fingerprint
        };

        private readonly byte[] refreshErrorResponseByteArray = new byte[] {
            0x01, 0x14, 0x00, 0x18, 0x21, 0x12, 0xa4, 0x42, 0x39, 0x50, 0x4d, 0x4b, 0x64, 0x63, 0x79, 0x30, 0x6e, 0x6c, 0x69, 0x58, // header
